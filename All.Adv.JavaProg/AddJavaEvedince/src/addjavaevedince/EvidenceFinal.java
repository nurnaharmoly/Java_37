    private void butAddToTableActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_butAddToTableActionPerformed
        // TODO add your handling code here:
        String id="", name = "",pw="", email = "", age = "0", gender = "", hobby = "", round = "",mag="";
        
        if(txtID.getText().length() <1){
            JOptionPane.showMessageDialog(null, "Enter Your ID");
        }
        else if(txtName.getText().length() <1){
            JOptionPane.showMessageDialog(null, "Enter Your Name");
        }
        else if(jpass.getText().length() <1){
            JOptionPane.showMessageDialog(null, "Enter Your PassWord"); 
            
        }else if(!checkEmailValidationNew(txtEmail.getText()) || txtEmail.getText().length() < 1){
            JOptionPane.showMessageDialog(null, "Enter Your Valid Email");
        }
        else if(Integer.parseInt(txtAge.getText()) < 18 || Integer.parseInt(txtAge.getText()) > 70){
        JOptionPane.showMessageDialog(null, "Enter Your Age");
        } 
        
        else if(buttonGroup1.getSelection().isSelected() == false){
            JOptionPane.showMessageDialog(null, "Enter Your Gender");
        }
        
        
         else if(cmbRound.getItemAt(cmbRound.getSelectedIndex()) == "Selected A Round"){
            JOptionPane.showMessageDialog(null, "Enter Your Round");
        }
         else if(txtAreaMeg.getText().length() <5){
            JOptionPane.showMessageDialog(null, "Enter Your Message");
        } else {
              id = txtID.getText();
              name = txtName.getText(); 
              age = txtAge.getText();
              pw = jpass.getText();
              email = txtEmail.getText();
              
              if(radMale.isSelected()){
              gender = radMale.getText();
              }
              
              if(radFemale.isSelected()){
              gender = radFemale.getText();
              }
              
             if(chkReading.isSelected()){
              hobby += chkReading.getText()+ " ";
              }
             
             if(chkWriting.isSelected()){
              hobby += chkWriting.getText()+" " ;
              }
             
             if(chkCoding.isSelected()){
              hobby += chkCoding.getText()+" ";
              }
             
             round = cmbRound.getItemAt(cmbRound.getSelectedIndex());
             mag = txtAreaMeg.getText();
             
             Student student = new Student(id, name, pw, email, Integer.parseInt(age), gender, hobby, round, mag);
             List<Student> students = new ArrayList<>();
             students.add(student);
             
           
              DefaultTableModel model = (DefaultTableModel) tblDisplay.getModel();
              Object []row = new Object[9];
              
              for(int i =0; i<students.size(); i++){
               row[0] = students.get(i).getID();
               row[1] = students.get(i).getName();
               row[2] = students.get(i).getPassWord();
               row[3] = students.get(i).getEmail();
               row[4] = students.get(i).getAge();
               row[5] = students.get(i).getGender();
               row[6] = students.get(i).getHobby();
               row[7] = students.get(i).getRound();
               row[8] = students.get(i).getMag();
               model.addRow(row);
               
                  try {
                      Utils.writeToFile("Moly", students);
                  } catch (Exception e) {
                       Logger.getLogger(EvidenceFinal.class.getName()).log(Level.SEVERE, null, e);
                  }
              
              }
            lblMsg.setText("Successfully Added into Table and Write to File");
             
             
         }
        
        
    }//GEN-LAST:event_butAddToTableActionPerformed

