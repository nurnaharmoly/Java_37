    private void bntAddTaFileActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bntAddTaFileActionPerformed
        // TODO add your handling code here:
        String id = "", name="", email="", age="0", gender="", hobby="", round="", address="", pw=""; 
        
        if(txtId.getText().length() <1){
            JOptionPane.showMessageDialog(null, "Enter your ID");
        }else if(txtName.getText().length() <1){
            JOptionPane.showMessageDialog(null, "Enter your Name");
        }else if(!checkEmailValidation(txtEmail.getText()) || txtEmail.getText().length()<1){
            JOptionPane.showMessageDialog(null, "Enter your Email");
        }else if(Integer.parseInt(txtAge.getText()) <18 || Integer.parseInt(txtAge.getText()) >70){
            JOptionPane.showMessageDialog(null, "Enter your Age");
        }else if(!chkReading.isSelected() && !chkWriting.isSelected() && !chkCoding.isSelected()){
            JOptionPane.showMessageDialog(null, "Enter your Hobby");
        }else if(cmbRound.getItemAt(cmbRound.getSelectedIndex())== "Select A Round"){
            JOptionPane.showMessageDialog(null, "Select your Round");    
        }else if(txtAreaAdd.getText().length() <5){
            JOptionPane.showMessageDialog(null, "Enter your Address");
        }else if(passwordFild.getText().length() <5){
            JOptionPane.showMessageDialog(null, "Enter your password");
        }else{    
        id = txtId.getText();
        name = txtName.getText();
        email = txtEmail.getText();
        age = txtAge.getText();
        
        if(rdMale.isSelected()){
        gender = rdMale.getText();
        }
        if(rdFemale.isSelected()){
        gender = rdFemale.getText();
        }
        if(chkReading.isSelected()){
        hobby += chkReading.getText()+" ";
        }
        if(chkWriting.isSelected()){
        hobby += chkWriting.getText()+" ";
        }
        if(chkCoding.isSelected()){
        hobby += chkCoding.getText()+" ";
        }
        round = cmbRound.getItemAt(cmbRound.getSelectedIndex());
        address = txtAreaAdd.getText();
        pw = passwordFild.getText();
        
        Student students = new Student(id, name, email, Integer.parseInt(age), gender, hobby, round, address, pw);
        List<Student> list = new ArrayList<>();
        list.add(students);
        
        DefaultTableModel model = (DefaultTableModel) tblDisplay.getModel();
        Object row[] = new Object[9];
        
        for (int i = 0; i < list.size(); i++) {
            row[0] = list.get(i).getId();
            row[1] = list.get(i).getName();
            row[2] = list.get(i).getEmail();
            row[3] = list.get(i).getAge();
            row[4] = list.get(i).getGender();
            row[5] = list.get(i).getHobby();
            row[6] = list.get(i).getRound();
            row[7] = list.get(i).getAddress();
            row[8] = list.get(i).getPassword();
            model.addRow(row);
            
            try {
                Utils.writeToFile("gibon", list);
            } catch (Exception e) {
            }
          lblMsg.setText("Successfully added into table and write to file");
        }
        }
    }//GEN-LAST:event_bntAddTaFileActionPerformed

